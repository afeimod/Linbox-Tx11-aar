package com.winfusion.core.image.bmp.exception;

public class BMPException extends Exception {

    public BMPException() {
        super();
    }

    public BMPException(String message) {
        super(message);
    }

    public BMPException(String message, Throwable cause) {
        super(message, cause);
    }

    public BMPException(Throwable cause) {
        super(cause);
    }
}
