package com.winfusion.core.mslink.exception;

public class ShellLinkException extends Exception {

    public ShellLinkException() {
        super();
    }

    public ShellLinkException(String message) {
        super(message);
    }

    public ShellLinkException(String message, Throwable cause) {
        super(message, cause);
    }

    public ShellLinkException(Throwable cause) {
        super(cause);
    }
}
