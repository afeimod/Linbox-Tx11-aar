package com.winfusion.core.shm.exception;

public class SHMDriverException extends Exception {

    public SHMDriverException() {
        super();
    }

    public SHMDriverException(String message) {
        super(message);
    }

    public SHMDriverException(String message, Throwable cause) {
        super(message, cause);
    }

    public SHMDriverException(Throwable cause) {
        super(cause);
    }
}
